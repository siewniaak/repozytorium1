class Biblioteka:
    def __init__(self, miasto, ulica, kod_pocztowy, godziny_otwarcia, telefon):
        self.miasto = miasto
        self.ulica = ulica
        self.kod_pocztowy = kod_pocztowy
        self.godziny_otwarcia = godziny_otwarcia
        self.telefon = telefon


    def __str__(self):
        return f"Biblioteka: {self.miasto}, {self.ulica}, {self.kod_pocztowy}\n" \
               f"Godziny otwarcia: {self.godziny_otwarcia}\nTelefon: {self.telefon}\n"


